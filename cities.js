// Функція для пошуку міст
function searchCities(query) {
    query = query.toLowerCase();
    let results = [];
    
    // Пошук по обласних центрах
    results = results.concat(cities.regionalCenters.filter(city => 
        city.toLowerCase().includes(query)
    ));
    
    // Пошук по інших містах України
    results = results.concat(cities.otherCities.filter(city => 
        city.toLowerCase().includes(query)
    ));
    
    // Пошук по закордонних містах
    Object.values(cities.foreignCities).forEach(countryCities => {
        results = results.concat(countryCities.filter(city => 
            city.toLowerCase().includes(query)
        ));
    });
    
    // Видаляємо дублікати та обмежуємо результати до 10
    return [...new Set(results)].slice(0, 10);
}

// Роблимо функції доступними глобально
window.cities = cities;
window.searchCities = searchCities;